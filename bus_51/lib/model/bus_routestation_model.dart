class BusRouteStationModel {
  final String centerYn;
  final int districtCd;
  final String mobileNo;
  final String regionName;
  final int stationId;
  final String stationName;
  final double x;
  final double y;
  final String adminName;
  final int stationSeq;
  final int turnSeq;
  final String turnYn;

  BusRouteStationModel({
